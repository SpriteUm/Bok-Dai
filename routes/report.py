from models import db
from models.issue import Issue
from models.issue_image import IssueImage  # ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏™‡πà‡∏ß‡∏ô‡∏ô‡∏µ‡πâ‡πÄ‡∏Ç‡πâ‡∏≤‡∏°‡∏≤

# ‡∏™‡∏£‡πâ‡∏≤‡∏á Blueprint
report_bp = Blueprint('report', __name__, template_folder='../templates')


# ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö/‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÇ‡∏ü‡∏•‡πÄ‡∏î‡∏≠‡∏£‡πå‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î
def ensure_upload_folder():
    folder = current_app.config.get('UPLOAD_FOLDER', 'static/uploads')
    os.makedirs(folder, exist_ok=True)
    return folder


# ‡∏ü‡∏≠‡∏£‡πå‡∏°‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô‡∏õ‡∏±‡∏ç‡∏´‡∏≤
class ReportForm(FlaskForm):
    category = SelectField('‡∏´‡∏±‡∏ß‡∏Ç‡πâ‡∏≠', choices=[
        ('', '-- ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà --'),
        ('‡πÇ‡∏Ñ‡∏£‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á/‡∏™‡∏¥‡πà‡∏á‡∏≠‡∏≥‡∏ô‡∏ß‡∏¢‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏î‡∏ß‡∏Å', '‡πÇ‡∏Ñ‡∏£‡∏á‡∏™‡∏£‡πâ‡∏≤‡∏á/‡∏™‡∏¥‡πà‡∏á‡∏≠‡∏≥‡∏ô‡∏ß‡∏¢‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏î‡∏ß‡∏Å'),
        ('‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏≠‡∏≤‡∏î', '‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏∞‡∏≠‡∏≤‡∏î'),
        ('‡∏Ñ‡∏ß‡∏≤‡∏°‡∏õ‡∏•‡∏≠‡∏î‡∏†‡∏±‡∏¢', '‡∏Ñ‡∏ß‡∏≤‡∏°‡∏õ‡∏•‡∏≠‡∏î‡∏†‡∏±‡∏¢'),
        ('‡πÑ‡∏ü‡∏ü‡πâ‡∏≤', '‡πÑ‡∏ü‡∏ü‡πâ‡∏≤'),
        ('‡∏≠‡∏∑‡πà‡∏ô‡πÜ', '‡∏≠‡∏∑‡πà‡∏ô‡πÜ')
    ], validators=[DataRequired()])
    other_text = StringField('‡∏≠‡∏∑‡πà‡∏ô‡πÜ')
    detail = TextAreaField('‡∏£‡∏≤‡∏¢‡∏•‡∏∞‡πÄ‡∏≠‡∏µ‡∏¢‡∏î', validators=[DataRequired()])
    date_reported = DateField('‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÄ‡∏Å‡∏¥‡∏î‡πÄ‡∏´‡∏ï‡∏∏', format='%Y-%m-%d', validators=[DataRequired()], default=datetime.utcnow)
    location_text = StringField('‡∏™‡∏ñ‡∏≤‡∏ô‡∏ó‡∏µ‡πà', validators=[DataRequired()])
    urgency = SelectField('‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏£‡πà‡∏á‡∏î‡πà‡∏ß‡∏ô', choices=[
        ('üî¥', '‡∏™‡∏π‡∏á‡∏™‡∏∏‡∏î'), ('üü†', '‡∏õ‡∏≤‡∏ô‡∏Å‡∏•‡∏≤‡∏á'), ('üü¢', '‡∏ï‡πà‡∏≥')
    ], validators=[DataRequired()])
    submit = SubmitField('‡∏™‡πà‡∏á‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô')


# ‡πÄ‡∏™‡πâ‡∏ô‡∏ó‡∏≤‡∏á‡∏Ç‡∏≠‡∏á‡∏´‡∏ô‡πâ‡∏≤ report
@report_bp.route('/', methods=['GET', 'POST'])
@login_required
def report():
    form = ReportForm()
    if form.validate_on_submit():
        try:
            # ‚úÖ ‡∏™‡∏£‡πâ‡∏≤‡∏á record ‡∏Ç‡∏≠‡∏á Issue
            issue = Issue(
                user_id=current_user.id,
                category=form.category.data,
                detail=form.detail.data,
                date_reported=form.date_reported.data or datetime.utcnow().date(),
                location_text=form.location_text.data,
                urgency=form.urgency.data,
                status='‡∏£‡∏≠‡∏î‡∏≥‡πÄ‡∏ô‡∏¥‡∏ô‡∏Å‡∏≤‡∏£',
                lat=None,
                lng=None
            )
            db.session.add(issue)
            db.session.flush()  # ‚úÖ flush ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ issue.id ‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡πÑ‡∏î‡πâ‡∏Å‡πà‡∏≠‡∏ô commit

            # ‚úÖ ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ)
            upload_folder = ensure_upload_folder()
            files = request.files.getlist('images[]')  # ‡∏ï‡πâ‡∏≠‡∏á‡∏°‡∏µ input ‡∏ä‡∏∑‡πà‡∏≠ images[] ‡πÉ‡∏ô HTML
            for file in files:
                if file and file.filename != '':
                    filename = file.filename
                    save_path = os.path.join(upload_folder, filename)
                    file.save(save_path)

                    # ‚úÖ ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å path ‡∏•‡∏á‡πÉ‡∏ô‡∏ï‡∏≤‡∏£‡∏≤‡∏á IssueImage
                    issue_image = IssueImage(
                        issue_id=issue.id,
                        file_path=save_path
                    )
                    db.session.add(issue_image)

            # ‚úÖ commit ‡∏ó‡∏∏‡∏Å‡∏≠‡∏¢‡πà‡∏≤‡∏á‡∏•‡∏á DB
            db.session.commit()

            flash("‡∏™‡πà‡∏á‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô‡πÅ‡∏•‡∏∞‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏£‡∏π‡∏õ‡∏†‡∏≤‡∏û‡πÄ‡∏£‡∏µ‡∏¢‡∏ö‡∏£‡πâ‡∏≠‡∏¢‡πÅ‡∏•‡πâ‡∏ß", "success")
            return redirect(url_for('indexuser'))

        except Exception:
            current_app.logger.exception("Error saving Issue or images")
            db.session.rollback()
            flash("‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏£‡∏≤‡∏¢‡∏á‡∏≤‡∏ô ‡∏î‡∏π log ‡πÉ‡∏ô terminal", "error")

    return render_template('report.html', form=form)
